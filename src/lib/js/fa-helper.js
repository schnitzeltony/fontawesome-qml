// helper for easy coloring
function icon(symbol, color) {
    var colorStart=""
    var colorEnd=""
    if(color !== null) {
        colorStart="<font color='"+color+"'>"
        colorEnd="</font>"
    }
    return colorStart+symbol+colorEnd
}

